
public class Student {
	private int PID;
	private String first;
	private String last;
	private String major;
	private String minor;
	
	//Set values based off of database//???????????????????????????????????????????????????
	public Student(int userPID)
	{
		PID = userPID;
		first = "Tyler";
		last = "Hoyt";
		major = "Computer Science";
		minor = ""; //Wont let me set it to null????????????????????????????????????????????
	}
	
	public int getPID()
	{
		return PID;
	}
	
	public String getFirst()
	{
		return first;
	}
	
	public String getLast()
	{
		return last;
	}
	
	public String getFull()
	{
		return (first + " " + last);
	}
	
	public String getMajor()
	{
		return major;
	}
	
	public String getMinor()
	{
		return minor;
	}
}
